<h2>Cấu trúc MVC:</h2>
<ul>
    <li><strong>Router:</strong> Điều hướng request đến đúng controller và action</li>
    <li><strong>Controller:</strong> Xử lý logic nghiệp vụ, tương tác với model, render view</li>
    <li><strong>View:</strong> Hiển thị giao diện, nhận data từ controller</li>
</ul>

<h2>OOP Concepts:</h2>
<ul>
    <li><strong>Class & Object:</strong> Định nghĩa và tạo instance</li>
    <li><strong>Inheritance:</strong> Kế thừa từ class cha</li>
    <li><strong>Interface:</strong> Định nghĩa contract</li>
    <li><strong>Abstract:</strong> Class không thể instantiate trực tiếp</li>
    <li><strong>Trait:</strong> Tái sử dụng code giữa các class</li>
</ul>

